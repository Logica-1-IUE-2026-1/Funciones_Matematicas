"""esta funcion es la que calcula el area y el perímetro de un cuadrado"""
def area_cuadrado(a):
    """
    Esta función calcula el área de un cuadrado.

    Parámetros:
    a (int): lado del cuadrado.
    
    Retorna:
    int: El área del cuadrado.
    """
    z= a*a
    return z

