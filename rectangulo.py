"""esta funcion es la que calcula el area y el perímetro de un rectangulo"""
def area_rectangulo(a,b):
    """
    Esta función calcula el área de un rectangulo.

    Parámetros:
    a (int): base del rectangulo.
    b (int): alto del rectangulo.
    
    Retorna:
    int: El área del rectangulo.
    """
    z= a*b
    return z

def perimetro_rectangulo(a,b):
    """
    Esta función calcula el perímetro de un rectangulo.

    Parámetros:
    a (int): base del rectangulo.
    b (int): alto del rectangulo.
    
    Retorna:
    int: El perímetro del rectangulo.
    """
    z= (a+b)*2
    return z
